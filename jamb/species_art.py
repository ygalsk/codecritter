# ── Helper Function ─────────────────────────────────────────────────


def get_frames(
    species: str,
    stage: str,
    specialization: str | None = None,
    eyes: str = "\u00b7",
) -> list[str]:
    """Look up frames for a species/stage/specialization, apply eye replacement.

    Falls back to snail if species not found.
    """
    # Build the art key
    if stage == "adult" and specialization:
        key = f"adult_{specialization}"
    else:
        key = stage

    # Look up species, fall back to snail
    species_data = SPECIES_ART.get(species, SPECIES_ART["snail"])

    # Look up the key within the species, fall back to hatchling
    frames = species_data.get(key, species_data.get("hatchling", [""]))

    # Replace {E} with the chosen eye style
    return [frame.replace("{E}", eyes) for frame in frames]
